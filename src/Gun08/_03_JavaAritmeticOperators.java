package Gun08;

public class _03_JavaAritmeticOperators {
    public static void main(String[] args) {
        int sayac=0;

        int a=1;
        sayac=sayac+1;   // sayacın değerini 1 artırır : 1
        sayac++;         // sayacın değerini 1 artırır : 2
        ++sayac;         // sayacın değerini 1 artırır : 3

        sayac = sayac-1; // sayacın değerini 1 azaltır : 2
        sayac--;         // sayacın değerini 1 azaltır : 1
        --sayac;         // sayacın değerini 1 azaltır : 0

        sayac+=1;
    }
}
