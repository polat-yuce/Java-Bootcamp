package Gun03;

public class _01_Degiskenler {
    public static void main(String[] args) {

        int toplam=0; // değişkeni tanımlarken ilk deger vermek mecburiyeti yok
        int sayac=0; // istersen ilk değeri tanımlarken verebilirsin

        System.out.println(sayac);

        sayac = sayac+2; //2  + : sayısal toplama yaptı
        System.out.println("sayac="+sayac);  // sayac=2  +: birleştirme yaptı

        System.out.println("sayac = " + sayac); // soutv ile yazma
        System.out.println("toplam = " + toplam);

    }
}
